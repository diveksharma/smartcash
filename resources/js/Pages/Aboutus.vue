<script>
import { Head } from '@inertiajs/inertia-vue3';
import HomepageLayout from './HomepageLayout';
/* import { MailIcon, PhoneIcon } from '@heroicons/vue/outline' */


  
  export default {
    components: {
      Head,
      HomepageLayout,
    },

    data() {
      return {
        fields: {},
        errors: {},
        success: false,
        loaded: true,
      };
    },

    methods: {
        submitContactForm() {
            if (this.loaded) {
                this.loaded = false;
                this.success = false;
                this.errors = {};

                axios.post('/submit-contact-form', this.fields)
                    .then(response => {
                        this.fields = {}; //Clear input fields.
                        this.loaded = true;
                        this.success = true;
                    })
                    .catch(error => {
                        this.loaded = true;
                        if (error.response.status === 422) {
                                this.errors = error.response.data.errors || {};
                        }
                    });
            }
        },
    },
        
  }

</script>
